/*### Vorteil des Service Layers

Ein Service Layer trennt die Geschäftslogik von den Datenklassen (Models).

Vorteile:

- Bessere Struktur – Code ist übersichtlicher organisiert.  
- Wiederverwendbarkeit – Die Logik kann von mehreren Teilen der Anwendung genutzt werden.  
- Einfachere Wartung – Änderungen müssen nur im Service und nicht in vielen Klassen gemacht werden.  
- Trennung der Verantwortung – Models enthalten nur Daten, Services enthalten die Logik.
*/
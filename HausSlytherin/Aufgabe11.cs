//Wann verwendet man Exceptions?
//Exceptions werden verwendet, um Fehler oder unerwartete Situationen in einem Programm zu 
//behandeln. Sie ermöglichen es, den normalen Ablauf eines Programms zu unterbrechen und
//stattdessen eine alternative Fehlerbehandlung durchzuführen.
//Hier sind einige Situationen, in denen Exceptions verwendet werden sollten:
//1. Fehlerhafte Benutzereingaben: Wenn ein Benutzer ungültige Daten eingibt, wie z.B. eine falsche Zahl oder einen ungültigen Dateipfad, können Exceptions verwendet werden, um diese Fehler zu erkennen und angemessen darauf zu reagieren.
//2. Netzwerkfehler: Wenn ein Programm versucht, eine Verbindung zu einem Server herzustellen